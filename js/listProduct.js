// Function to display product details
function detailProduct() {
  const prdId = new URLSearchParams(window.location.search).get("id");

  if (prdId) {
    let product;
    let detailProductDiv = document.querySelector(".left-detail");

    product = listSnCongNghiep.find(item => item.id == prdId);

    if (!product) {
      product = listXayDung.find(item => item.id == prdId);
    }

    if (!product) {
      product = listxdcn.find(item => item.id == prdId);
    }

    if (!product) {
      product = listhoachat.find(item => item.id == prdId);
    }

    if (product) {
      let relatedImagesOut = ``;
      product.relatedImages.forEach(img => {
        relatedImagesOut += `<img src="${img}" width="77%" alt="">`;
      });
      detailProductDiv.innerHTML = relatedImagesOut;
    } else {
      console.log("Không tìm thấy sản phẩm");
    }
  } else {
    console.log("Không tìm thấy sản phẩm");
  }
}

